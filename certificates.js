let currentView = "grid";
let currentCategory = "";
let filteredCertificates = [];

// Initialize the gallery
document.addEventListener("DOMContentLoaded", function () {
  setupEventListeners();
  //   updateStats();
});

function setupEventListeners() {
  document.querySelectorAll(".category-card").forEach((card) => {
    card.addEventListener("click", function () {
      const category = this.getAttribute("data-category");
      showCategory(category);
    });
  });

  // View toggle
  document.querySelectorAll(".view-btn").forEach((btn) => {
    btn.addEventListener("click", function () {
      const view = this.getAttribute("data-view");
      toggleView(view);
    });
  });

  // Search functionality
  document.getElementById("searchInput").addEventListener("input", function () {
    const searchTerm = this.value.toLowerCase();
    filterCertificates(searchTerm);
  });

  // Modal close on backdrop click
  document
    .getElementById("certificateModal")
    .addEventListener("click", function (e) {
      if (e.target === this) {
        closeModal();
      }
    });
}

function showCategory(category) {
  currentCategory = category;
  const certificates = certificateData[category] || [];
  filteredCertificates = certificates;

  document.querySelector(".category-overview").style.display = "none";
  document.getElementById("certificatesContainer").classList.add("active");
  document.getElementById("currentCategoryTitle").textContent =
    categoryNames[category];

  renderCertificates(certificates);
}

function showCategoryOverview() {
  document.querySelector(".category-overview").style.display = "block";
  document.getElementById("certificatesContainer").classList.remove("active");
  document.getElementById("searchInput").value = "";
}

function renderCertificates(certificates) {
  const gridContainer = document.getElementById("certificatesGrid");
  const listContainer = document.getElementById("certificatesList");

  gridContainer.innerHTML = "";
  listContainer.innerHTML = "";

  if (certificates.length === 0) {
    document.getElementById("noResults").style.display = "block";
    return;
  }

  document.getElementById("noResults").style.display = "none";

  certificates.forEach((cert) => {
    // Grid view
    const gridItem = createCertificateCard(cert);
    gridContainer.appendChild(gridItem);

    // List view
    const listItem = createCertificateListItem(cert);
    listContainer.appendChild(listItem);
  });
}

function createCertificateCard(cert) {
  const card = document.createElement("div");
  card.className = "certificate-card";
  card.onclick = () => openModal(cert);

  card.innerHTML = `
                <div class="certificate-image"></div>
                <div class="certificate-info">
                    <div class="certificate-title">${cert.title}</div>
                    <div class="certificate-filename">${cert.filename}</div>
                </div>
            `;

  return card;
}

function createCertificateListItem(cert) {
  const item = document.createElement("div");
  item.className = "certificate-list-item";
  item.onclick = () => openModal(cert);

  const fileExt = cert.filename.split(".").pop().toUpperCase();

  item.innerHTML = `
                <div class="list-item-icon">${fileExt}</div>
                <div class="list-item-content">
                    <div class="list-item-title">${cert.title}</div>
                    <div class="list-item-filename">${cert.filename}</div>
                </div>
            `;

  return item;
}

function toggleView(view) {
  currentView = view;

  // Update button states
  document.querySelectorAll(".view-btn").forEach((btn) => {
    btn.classList.remove("active");
  });
  document.querySelector(`[data-view="${view}"]`).classList.add("active");

  // Toggle containers
  if (view === "grid") {
    document.getElementById("certificatesGrid").style.display = "grid";
    document.getElementById("certificatesList").style.display = "none";
  } else {
    document.getElementById("certificatesGrid").style.display = "none";
    document.getElementById("certificatesList").style.display = "flex";
  }
}

function filterCertificates(searchTerm) {
  if (!currentCategory) return;

  const allCertificates = certificateData[currentCategory] || [];
  filteredCertificates = allCertificates.filter(
    (cert) =>
      cert.title.toLowerCase().includes(searchTerm) ||
      cert.filename.toLowerCase().includes(searchTerm)
  );

  renderCertificates(filteredCertificates);
}

function openModal(cert) {
  document.getElementById("modalTitle").textContent = cert.title;
  document.getElementById("modalFilename").textContent = cert.filename;
  document.getElementById("modalCategory").textContent =
    categoryNames[currentCategory];
  document.getElementById("certificateModal").style.display = "block";

  // You can customize this to show actual certificate preview
  const fileExt = cert.filename.split(".").pop().toLowerCase();
  const modalCertificate = document.getElementById("modalCertificate");
  modalCertificate.innerHTML = "";

  if (fileExt === "pdf") {
    modalCertificate.innerHTML = `<iframe src="certificates/${cert.filename}" style="width:100%;height:500px;border:none;"></iframe>`;
  } else {
    modalCertificate.innerHTML = `<img src="certificates/${cert.filename}" alt="${cert.title}" style="max-width:100%;max-height:500px;border-radius:8px;" />`;
  }
}

function closeModal() {
  document.getElementById("certificateModal").style.display = "none";
}

function downloadCertificate() {
  const filename = document.getElementById("modalFilename").textContent;
  const link = document.createElement("a");
  link.href = `certificates/${filename}`;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
}

